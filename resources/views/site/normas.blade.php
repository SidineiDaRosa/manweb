  <h1>Normas que Regem a Manutenção</h1>
  <p>A manutenção eficiente é essencial para garantir a confiabilidade, segurança e longevidade dos ativos de uma organização. Abaixo destacamos as principais normas que orientam essas práticas.</p>

  <section>
    <h2>1. ISO 55000 – Gestão de Ativos</h2>
    <p>Objetivo: Estabelecer uma visão estratégica para a gestão de ativos, visando maximizar o valor ao longo do ciclo de vida.</p>
    <p>Aplicação: Orienta organizações na implementação de sistemas eficazes de gestão de ativos.</p>
    <p>Fonte: <a href="https://www.iso.org/standard/55088.html" target="_blank">ISO 55000:2014</a></p>
  </section>

  <section>
    <h2>2. ISO 9001 – Sistema de Gestão da Qualidade</h2>
    <p>Objetivo: Definir os requisitos para um sistema de gestão da qualidade, com foco na satisfação do cliente e melhoria contínua.</p>
    <p>Aplicação: Inclui manutenção preventiva como parte do sistema de gestão para reduzir falhas e aumentar eficiência.</p>
    <p>Fonte: <a href="https://www.iso.org/standard/62085.html" target="_blank">ISO 9001:2015</a></p>
  </section>

  <section>
    <h2>3. ISO 14224 – Coleta e Intercâmbio de Dados de Confiabilidade</h2>
    <p>Objetivo: Padronizar a coleta e intercâmbio de dados de confiabilidade e manutenção, especialmente em indústrias de petróleo e gás.</p>
    <p>Fonte: <a href="https://www.iso.org/standard/64076.html" target="_blank">ISO 14224:2016</a></p>
  </section>

  <section>
    <h2>4. ABNT NBR 5462 – Confiabilidade e Mantenabilidade</h2>
    <p>Objetivo: Fornecer orientações técnicas e administrativas para estratégias de manutenção, incluindo classificação de falhas.</p>
    <p>Fonte: <a href="https://ufsb.edu.br/propa/images/dinfra/coman/Legisla%C3%A7%C3%B5es/NBR-5462.pdf" target="_blank">NBR 5462</a></p>
  </section>

  <section>
    <h2>5. NR-12 – Segurança em Máquinas e Equipamentos</h2>
    <p>Objetivo: Garantir a saúde e integridade física dos trabalhadores envolvidos com máquinas e equipamentos.</p>
    <p>Fonte: <a href="https://www.gov.br/trabalho-e-emprego/pt-br/acesso-a-informacao/participacao-social/conselhos-e-orgaos-colegiados/comissao-tripartite-partitaria-permanente/arquivos/normas-regulamentadoras/nr-12-atualizada-2022-1.pdf" target="_blank">NR-12</a></p>
  </section>

  <section>
    <h2>6. NR-13 – Caldeiras, Vasos de Pressão e Tanques Metálicos</h2>
    <p>Objetivo: Estabelecer requisitos para a integridade estrutural e segurança de caldeiras, vasos de pressão e equipamentos relacionados.</p>
    <p>Fonte: <a href="https://www.gov.br/trabalho-e-emprego/pt-br/acesso-a-informacao/participacao-social/conselhos-e-orgaos-colegiados/comissao-tripartite-partitaria-permanente/arquivos/normas-regulamentadoras/nr-13-atualizada-2022-retificada.pdf" target="_blank">NR-13</a></p>
  </section>

  <section>
    <h2>Conclusão</h2>
    <p>A adoção dessas normas garante redução de custos, aumento da vida útil dos ativos, melhoria na segurança do trabalho e conformidade legal. Seguir essas diretrizes é essencial para uma gestão de manutenção eficaz e segura.</p>
  </section>

