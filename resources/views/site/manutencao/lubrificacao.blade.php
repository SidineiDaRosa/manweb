    <section id="introducao" class="section">
        <h2>1. Introdução</h2>
        <p>A lubrificação é um dos pilares da manutenção industrial, responsável por reduzir o atrito e o desgaste entre superfícies em movimento. Sua correta aplicação garante o funcionamento adequado dos equipamentos, aumenta a vida útil dos componentes e melhora a segurança operacional.</p>
    </section>

    <section id="funcao" class="section">
        <h2>2. Função da lubrificação</h2>
        <p>As funções principais da lubrificação incluem:</p>
        <ul>
            <li>Reduzir o atrito entre superfícies móveis;</li>
            <li>Diminuir a geração de calor e desgaste;</li>
            <li>Prevenir a corrosão e a oxidação;</li>
            <li>Servir como selo contra contaminações (poeira, umidade);</li>
            <li>Melhorar o desempenho e reduzir ruídos.</li>
        </ul>
    </section>

    <section id="consequencias" class="section">
        <h2>3. Consequências da falta de lubrificação</h2>
        <p>A negligência com a lubrificação pode causar impactos operacionais e financeiros consideráveis:</p>
        <ul>
            <li>Aumento do consumo energético devido ao atrito extra;</li>
            <li>Sobreaquecimento e falhas em rolamentos, eixos e engrenagens;</li>
            <li>Paradas não programadas, reduzindo a disponibilidade produtiva;</li>
            <li>Custos elevados com reparos e substituições prematuras;</li>
            <li>Risco aumentado de acidentes e perda de segurança no trabalho.</li>
        </ul>
    </section>

    <section id="tipos" class="section">
        <h2>4. Tipos de lubrificantes</h2>
        <p>Os lubrificantes mais usados na indústria são:</p>
        <ul>
            <li><strong>Óleos lubrificantes</strong> — ideais para sistemas que precisam de fluidez e dissipação de calor (ex.: caixas de engrenagens, sistemas hidráulicos);</li>
            <li><strong>Graxas</strong> — recomendadas em pontos de difícil reposição, onde é necessária retenção do lubrificante (ex.: mancais, pontos de pivotamento);</li>
            <li><strong>Lubrificantes sólidos</strong> — como grafite ou bisulfeto de molibdênio, úteis em altas temperaturas ou pressões extremas;</li>
        </ul>
        <p>A escolha do lubrificante depende de temperatura de operação, velocidade, carga, ambiente (contaminação) e compatibilidade com materiais.</p>
    </section>

    <section id="plano" class="section">
        <h2>5. A importância do plano de lubrificação</h2>
        <p>Um plano de lubrificação bem estruturado deve conter:</p>
        <ul>
            <li>Lista de pontos de lubrificação com identificação clara (mapa de lubrificação);</li>
            <li>Tipo de lubrificante recomendado para cada ponto;</li>
            <li>Intervalos de lubrificação (hora, turno, km, ciclo de uso);</li>
            <li>Procedimentos de aplicação e quantidades indicadas;</li>
            <li>Registros de execução (quem executou, data, quantidade e observações);</li>
            <li>Critérios de monitoramento e inspeção (análises de óleo, verificação de temperatura, ruído etc.).</li>
        </ul>
        <p>O registro e o controle (manual ou digital) permitem ações preditivas e evitam excessos ou faltas de lubrificante.</p>
    </section>

    <section id="conclusao" class="section">
        <h2>6. Conclusão</h2>
        <p>Práticas corretas de lubrificação representam um investimento estratégico na confiabilidade e eficiência das operações. Empresas que mantêm rotinas de lubrificação adequadas apresentam menor custo operacional, maior disponibilidade de equipamentos e maior segurança no ambiente de trabalho.</p>
    </section>

